                        <h5 class="text-block-title">What is a CT SCAN?</h5>
                        <p class="text-block-desc mb-20">A CT scan, or Computed Tomography scan, is a medical imaging
                            procedure that uses X-rays and computer technology to create detailed cross-sectional images
                            of the body. It provides detailed information about the internal structures, allowing
                            healthcare professionals to diagnose and monitor various medical conditions. CT scans are
                            versatile and can be performed on different body parts to aid in the detection and
                            evaluation of a wide range of conditions.
                        </p>
                    </div>
                    <div class="text-block mb-50">
                        <h5 class="text-block-title">Why is an CT SCAN important?</h5>
                        <p class="text-block-desc mb-20">CT scans play a vital role in diagnosing and guiding treatment
                            plans for numerous medical conditions, providing detailed imaging, rapid results,
                            versatility, and assisting in guiding interventions.
                        </p>
                    </div>
                    <div class="text-block mb-50">
                        <h5 class="text-block-title">What to expect during the CT SCAN test?</h5>
                        <p class="text-block-desc mb-20">Before undergoing a CT scan, the patient will be requested to
                            remove any metal objects, such as jewelry and accessories, and change into a hospital gown.
                            Once prepared, the patient will be comfortably positioned on a movable table, which will
                            smoothly slide into the tunnel-shaped CT scanner. It's important to remain still during the
                            scan, which can last between 30 minutes to an hour, as the machine captures detailed images
                            of the body. The radiology technologist will be present to monitor the patient and provide
                            any necessary instructions or support throughout the procedure.
                        </p>
